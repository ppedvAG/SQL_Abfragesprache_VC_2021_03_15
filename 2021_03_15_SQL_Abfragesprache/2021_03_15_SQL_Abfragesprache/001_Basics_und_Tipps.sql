/*
		
		
		F5 - Execute/Ausführen


		STRG + R - Öffnen/Schließen des Resultfensters


		STRG + Leer - Vorschläge


		STRG + N - erstellt neues Query-Fenster


		STRG + Tab - zwischen einzelnen Tabs hin- und herspringen


		STRG + SHIFT + V  - toggle zwischen letzten Einträgen im Zwischenspeicher

		
		STRG + F - Text finden 
		STRG + G - bestimmte Zeile finden und dorthin navigieren 
		STRG + H - finden und ersetzen


		STRG + W - Wort markieren

		SHIFT + END - bis zum Ende der Zeile markieren
		SHIFT + Pos1 - bis zum Anfgang der Zeile markieren
		SHIFT + UPARROW/DOWNARROW - Zeile rauf/runter markieren


		STRG + K, STRG + K -- Bookmark (Lesezeichen) setzen oder entfernen
		STRG + K, STRG + N -- (next) springe zum nächsten Bookmark
		STRG + K, STRG + P -- (previous) springe zum vorherigen Bookmark


		-- Groß-/Kleinschreibung
		STRG + SHIFT + U -- UPPERCASE
		STRG + SHIFT + L -- lowercase



*/




